    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
         try{
               Connection conn = koneksi.getConnection();
               int row = tabel.getSelectedRow();
               String tabel_klik = (tabel.getModel().getValueAt(row, 0).toString());
               java.sql.Statement stmt = conn.createStatement();
               java.sql.ResultSet sql = stmt.executeQuery("select * from tb_pc where  kode_pc = '"+tabel_klik+"'");
               if (sql.next()){
                   String kode_pc=sql.getString("kode_pc");
                   textkode.setText(kode_pc);
                   String merk=sql.getString("merk");
                   textmerk.setText(merk);
                   String warna=sql.getString("warna");
                   textwarna.setText(warna);
               }
           }catch (SQLException e){}
    }//GEN-LAST:event_tabelMouseClicked
       
