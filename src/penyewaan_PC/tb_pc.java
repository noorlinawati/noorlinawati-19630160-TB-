    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int i =tabel.getSelectedRow();
        if(i== -1) {
            return;
        }
        
        String kode_pc = (String)mod.getValueAt(i, 0);
        int question = JOptionPane.showConfirmDialog(null, "Apakah Anda Yakin Ingin Menghapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if(question == JOptionPane.OK_OPTION){
            try{
                Connection c = koneksi.getConnection();
                String sql = "DELETE FROM tb_pc WHERE kode_pc= ?";
                try (PreparedStatement p = c.prepareStatement(sql)) {
                    p.setString(1, kode_pc);
                    p.executeUpdate();
                }
                JOptionPane.showMessageDialog(null, "Data Dihapus");
            }catch (SQLException e){
                System.out.println(e);
            }
        }
        if (question == JOptionPane.CANCEL_OPTION){
            
        }
    }//GEN-LAST:event_btndeleteActionPerformed

