    private void tampilData(){
        mod = new DefaultTableModel();
        mod.addColumn("id");
        mod.addColumn("nama_penyw");
        mod.addColumn("alamat_penyw");
        mod.addColumn("jenis_kelamin");
        mod.addColumn("tgl_lahir");
        mod.addColumn("no_hp");
        mod.addColumn("tgl_penyw");
        mod.addColumn("merk");
        jTable1.setModel(mod);
        Connection conn=koneksi.getConnection();
        try{
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select*from tb_penyewaan";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while(res.next()){
                mod.addRow(new Object[]{
                    res.getString("id"),
                    res.getString("nama_penyw"),
                    res.getString("alamat_penyw"),
                    res.getString("jenis_kelamin"),
                    res.getString("tgl_lahir"),
                    res.getString("no_hp"),
                    res.getString("tgl_penyw"),
                    res.getString("merk"),
                });
            }
        }catch (SQLException e){
            System.out.println(e);
        }
    }

